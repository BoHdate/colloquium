"""
1. Введіть з клавіатури в масив п'ять цілочисельних значень. Виведіть їх в
один рядок через кому. Отримайте для масиву середнє арифметичне.
"""

import numpy as np
x = list()
for i in range(1,6):                 #Виводимо 5 чисел
    a = int(input(f'Число {i} = '))
    x.append(a)                      #Додаємо числа у список 
j = np.array(x)                      #Створюємо масив з отриманого списку 
print(j)
print(f'Середнє значення: {j.mean()}')
