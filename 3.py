import numpy as np
x = list()
for i in range(1,6):                  #Виводимо призвіща 
    a = input(f'Прізвище {i}: ')
    x.append(a)                       #Додажмо їх у список 
x.sort(reverse = True)                #Сортуємо у зворотньому порядку
b = np.array(x).reshape(5,1)          #Формативне представлення
print(b)
